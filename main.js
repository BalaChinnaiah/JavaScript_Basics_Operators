// OPERATORS

// Assignment Ops
let x=10
let y = 5
// Arithmetic ops
console.log(x/y)
console.log(x+y)
console.log(x-y)
console.log(x%y)
console.log(x*y)

console.log(++x)
console.log(--y)
console.log(y--)
console.log(y)

// Comparision ops

console.log(x==y)
console.log(x!=y)
console.log(x===y)
console.log(x!==y)
console.log(x>=y)
console.log(x<=y)
console.log(x<y)
console.log(x>y)

// Logical Ops
const isTrue = x >10 && 8>y 
console.log(isTrue)

const isFalse = x >10 || 8>y 
console.log(isFalse)
console.log(!isFalse)

// Sring ops
console.log('Bala '+'Sagar')

